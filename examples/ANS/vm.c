uint32_t FetchCell(uint32_t addr) {
    if (addr & 3) {
        exception = -23;
    }
	uint32_t ca = addr >> 2;
    if (ca < ROMsize) {
#ifdef EmbeddedROM
        return (FetchROM(ca));
#else
        return (ROM[ca]);
#endif // EmbeddedROM
    }
	ca -= ROMsize;
    if (ca < RAMsize) {
        return (RAM[ca]);
    }
    return (FlashRead(addr));
}
